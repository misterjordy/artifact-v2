async def test_flat_search_returns_ranked_results(
    db: AsyncSession, admin_user: FcUser, child_node: FcNode,
) -> None:
    """Flat search returns ranked results matching the query."""
    await _publish_fact(
        db, child_node, "The system owner is responsible for configuration", admin_user,
    )
    await _publish_fact(db, child_node, "Network interfaces must be documented", admin_user)
    await _publish_fact(
        db, child_node, "The system owner reviews all changes to the system", admin_user,
    )
    results = await search_facts_flat(db, "system owner")
    assert len(results) >= 2
    for i in range(len(results) - 1):
        assert results[i].rank >= results[i + 1].rank


async def test_flat_search_breadcrumbs(
    db: AsyncSession, admin_user: FcUser, root_node: FcNode, child_node: FcNode,
) -> None:
    """Flat search breadcrumbs come from in-memory tree load."""
    await _publish_fact(
        db, child_node, "The system configuration is validated annually", admin_user,
    )
    results = await search_facts_flat(db, "system configuration")
    assert len(results) >= 1
    bc = results[0].breadcrumb
    assert len(bc) >= 2
    titles = [b.title for b in bc]
    assert root_node.title in titles
    assert child_node.title in titles
