async def _publish_fact(
    db: AsyncSession,
    node: FcNode,
    sentence: str,
    user: FcUser,
    *,
    smart_tags: list[str] | None = None,
    smart_tags_text: str = "",
) -> tuple[FcFact, FcFactVersion]:
    """Create a fact with a published version (search_vector generated by PostgreSQL)."""
    fact = FcFact(
        fact_uid=uuid.uuid4(),
        node_uid=node.node_uid,
        created_by_uid=user.user_uid,
    )
    db.add(fact)
    await db.flush()

    version = FcFactVersion(
        version_uid=uuid.uuid4(),
        fact_uid=fact.fact_uid,
        state="published",
        display_sentence=sentence,
        created_by_uid=user.user_uid,
        published_at=datetime.now(timezone.utc),
        smart_tags=smart_tags or [],
        smart_tags_text=smart_tags_text,
    )
    db.add(version)
    await db.flush()

    fact.current_published_version_uid = version.version_uid
    await db.flush()
    return fact, version


