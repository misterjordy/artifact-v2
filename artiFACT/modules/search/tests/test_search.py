async def test_get_program_for_node_finds_ancestor(
    db: AsyncSession,
    admin_user: FcUser,
    portfolio_root: FcNode,
    portfolio_program: FcNode,
    portfolio_leaf: FcNode,
) -> None:
    """get_program_for_node walks up to find is_program=True ancestor."""
    from sqlalchemy import select as sa_select

    all_result = await db.execute(
        sa_select(FcNode).where(FcNode.is_archived.is_(False))
    )
    all_nodes = list(all_result.scalars().all())

    program = get_program_for_node(all_nodes, portfolio_leaf.node_uid)
    assert program is not None
    assert program.node_uid == portfolio_program.node_uid
    assert program.title == "Project Alpha"


