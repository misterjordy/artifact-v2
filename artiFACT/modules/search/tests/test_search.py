async def test_search_excludes_retired_facts(
    db: AsyncSession, admin_user: FcUser, child_node: FcNode,
) -> None:
    """Retired facts are excluded from results."""
    await _publish_fact(
        db, child_node, "Active system uses cryptography standards", admin_user,
    )
    # Create a retired fact
    retired_fact = FcFact(
        fact_uid=uuid.uuid4(),
        node_uid=child_node.node_uid,
        created_by_uid=admin_user.user_uid,
        is_retired=True,
        retired_at=datetime.now(timezone.utc),
    )
    db.add(retired_fact)
    await db.flush()
    retired_version = FcFactVersion(
        version_uid=uuid.uuid4(),
        fact_uid=retired_fact.fact_uid,
        state="published",
        display_sentence="Retired fact about cryptography standards",
        created_by_uid=admin_user.user_uid,
        published_at=datetime.now(timezone.utc),
    )
    db.add(retired_version)
    await db.flush()
    retired_fact.current_published_version_uid = retired_version.version_uid
    await db.flush()

    result = await search_facts(db, "cryptography standards")
    all_sentences = [
        r["display_sentence"] for g in result["programs"] for r in g["results"]
    ]
    assert any("Active" in s for s in all_sentences)
    assert not any("Retired" in s for s in all_sentences)


