async def test_search_finds_fact_by_sentence_text(
    db: AsyncSession, admin_user: FcUser, child_node: FcNode,
) -> None:
    """Search finds facts matching sentence text."""
    await _publish_fact(
        db, child_node, "All data at rest is encrypted using RDS AES-256", admin_user,
    )
    result = await search_facts(db, "encrypted")
    assert result["total"] >= 1
    sentences = [
        r["display_sentence"] for g in result["programs"] for r in g["results"]
    ]
    assert any("encrypted" in s.lower() for s in sentences)


